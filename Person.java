public class Person {
  
  private int id;
  private String ad;
  private String soyad;
  
  public int getId() {
    return this.id;
  }
  
  public void setId(int id) {
    this.id = id;
  }
  
  public String getAd() {
    return this.ad;
  }
  
  public String getSoyad() {
    return this.soyad;
  }
  
  public void setAd(String ad) {
    this.ad = ad;
  }
  
  public void setSoyad(String soyad) {
    this.soyad = soyad;
  }
}
